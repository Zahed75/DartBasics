void main() {
  var pLanguage = ["Dart", "Python", "JavaScript", "C", "C++"];
  var result = pLanguage.length;
  var res = pLanguage.first;
  var re = pLanguage.last;
  var r = pLanguage.reversed;
  var check = pLanguage.isEmpty;
  var again = pLanguage.isNotEmpty;
  // single item check only valid when data is a single item
  var dataCheck = pLanguage.single;
  print(result);
  print(res);
  print(re);
  print(r);
  print(check);
  print(again);
  print(dataCheck);
}
